export function renderSidebar(container) {
  let html = `
    <div class="sidebar-logo">
      <div class="sidebar-logo-icon">
        <svg xmlns="http://www.w3.org/2000/svg" width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round">
          <path d="M6.5 6.5h11M6.5 17.5h11M4 10v4M8 8v8M16 8v8M20 10v4"/>
        </svg>
      </div>
      <span class="sidebar-logo-text">GymPro</span>
    </div>
    <div class="sidebar-nav">
  `;

  for (const group of NAV_GROUPS) {
    html += `<div class="sidebar-group">`;
    html += `<div class="sidebar-group-title">${group.title}</div>`;
    for (const item of group.items) {
      html += `
        <a class="sidebar-item" href="#${item.path}" data-path="${item.path}" aria-label="${item.label}">
          <span class="sidebar-item-icon">${ICONS[item.icon] || ''}</span>
          <span class="sidebar-item-label">${item.label}</span>
        </a>
      `;
    }
    html += `</div>`;
  }

  html += `
    </div>
    <div class="sidebar-footer">
      <button class="sidebar-item" id="sidebar-logout" aria-label="Logout">
        <span class="sidebar-item-icon">${ICONS['log-out']}</span>
        <span class="sidebar-item-label">Logout</span>
      </button>
      <button class="sidebar-collapse-btn" id="sidebar-collapse" aria-label="Toggle sidebar">
        ${ICONS['chevrons-left']}
      </button>
    </div>
  `;

  container.innerHTML = html;

  // Collapse toggle
  document.getElementById('sidebar-collapse').addEventListener('click', () => {
    const sidebar = document.getElementById('sidebar');
    const shell = document.getElementById('app-shell');
    sidebar.classList.toggle('collapsed');
    shell.classList.toggle('sidebar-collapsed');
  });

  // Logout
  document.getElementById('sidebar-logout').addEventListener('click', () => {
    logout();
    router.navigate('/login');
  });
}

export function updateSidebarActive(path) {
  const items = document.querySelectorAll('.sidebar-item[data-path]');
  items.forEach(item => {
    if (item.dataset.path === path) {
      item.classList.add('active');
    } else {
      item.classList.remove('active');
    }
  });
}
